    <h2>📋 Текст тестового задания</h2>
    
    <p>Есть база данных для хранения информации о клиентах, товарах и заказах со следующей структурой:</p>
    
    <div class="db-schema">
        <strong>clients</strong> (id, name) — ID клиента и его имя<br>
        <strong>merchandise</strong> (id, name) — ID товара и его наименование<br>
        <strong>orders</strong> (id, item_id, customer_id, comment, status, order_date) — ID заказа, ID товара, ID клиента, комментарий клиента, статус заказа (<code>'new'</code>, <code>'complete'</code>), дата заказа.
    </div>
    
    <p><em>* Структура предполагает, что один заказ — это один товар.</em></p>

    <h3>Необходимо реализовать:</h3>
    <ol>
        <li>
            <strong>Скрипт импорта данных:</strong><br>
            Написать скрипт, который получает на вход текстовый файл с данными о заказах (разделитель <code>";"</code>) вида: 
            <code>ID товара;ID клиента;Комментарий к заказу</code> и загружает содержимое в структуру БД. 
            При этом все невалидные строки должны записываться в отдельный файл. Использование сторонних решений / библиотек нежелательно.
        </li>
        <li>
            <strong>Аналитические SQL-запросы:</strong><br>
            Написать SQL-запросы, возвращающие набор данных, соответствующий следующим условиям:
            <ul>
                <li><code>a.</code> Выбрать имена (name) всех клиентов, которые не делали заказы в последние 7 дней.</li>
                <li><code>b.</code> Выбрать имена (name) 5 клиентов, которые сделали больше всего заказов в магазине.</li>
                <li><code>c.</code> Выбрать имена (name) 10 клиентов, которые сделали заказы на наибольшую сумму.</li>
                <li><code>d.</code> Выбрать имена (name) всех товаров, по которым не было доставленных заказов (со статусом <code>'complete'</code>).</li>
            </ul>
        </li>
        <li>
            <strong>Оптимизация и индексы:</strong><br>
            Описать, какие бы вы создали индексы для оптимизации скорости работы запросов из п.2 и почему.
        </li>
    </ol>

    <h3>📦 Состав решения (Выдаваемый архив):</h3>
    <ul>
        <li>Скрипт импорта заказов из п.1.</li>
        <li>Набор тестовых данных для скрипта, соответствующий описанному формату.</li>
        <li>Файл с SQL-запросами и перечнем необходимых индексов (с обоснованием).</li>
    </ul>
